/**
 * 定位用戶位置
 */
function locateUser() {
  if (navigator.geolocation && mapInstance.value) {
    navigator.geolocation.getCurrentPosition(
      (position) => {
        const { latitude, longitude } = position.coords;
        mapInstance.value!.setView([latitude, longitude], 15);
        ElMessage.success(t("emap.locationSuccess"));
      },
      (error) => {
        console.error("獲取位置失敗:", error);
        ElMessage.warning(t("emap.locationFailed"));
      }
    );
  } else {
    ElMessage.warning(t("emap.geolocationNotSupported"));
  }
}

/**
 * 將地圖中心定位到設備位置
 */
function centerMapOnDevices() {
  if (!mapInstance.value || deviceMarkers.value.length === 0) return;

  // 創建包含所有標記的邊界
  const group = L.featureGroup(deviceMarkers.value as unknown as L.Layer[]);
  mapInstance.value.fitBounds(group.getBounds().pad(0.1));
}

/**
 * 切换到英文地图
 */
function switchToEnglishMap() {
  if (!mapInstance.value || !mapLayers.value.cartoLight) return;

  // 移除当前图层
  mapInstance.value.eachLayer((layer) => {
    if (layer instanceof L.TileLayer) {
      mapInstance.value!.removeLayer(layer);
    }
  });

  // 添加英文地图图层
  mapLayers.value.cartoLight.addTo(mapInstance.value);
  currentMapStyle.value = "english";

  ElMessage.success("已切换到英文地图 (CartoDB Light)");
}

/**
 * 切换到混合地图
 */
function switchToMixedMap() {
  if (!mapInstance.value || !mapLayers.value.osm) return;

  // 移除当前图层
  mapInstance.value.eachLayer((layer) => {
    if (layer instanceof L.TileLayer) {
      mapInstance.value!.removeLayer(layer);
    }
  });

  // 添加OpenStreetMap图层
  mapLayers.value.osm.addTo(mapInstance.value);
  currentMapStyle.value = "mixed";

  ElMessage.success("已切换到标准地图 (OpenStreetMap)");
}

/**
 * 工具函數
 */
function getStatusTagType(status: string): "success" | "warning" | "danger" | "info" | "primary" {
  switch (status) {
    case "ACTIVE":
      return "success";
    case "INACTIVE":
      return "warning";
    default:
      return "info";
  }
}

function getStatusText(status: string): string {
  switch (status) {
    case "ACTIVE":
      return t("device.active");
    case "INACTIVE":
      return t("device.inactive");
    default:
      return status;
  }
}

function getDeviceModelText(deviceModel: string): string {
  switch (deviceModel) {
    case "WATER_LEVEL_SENSOR":
      return t("device.waterLevelSensor");
    case "OTHER":
      return t("device.otherDevice");
    default:
      return deviceModel;
  }
}

